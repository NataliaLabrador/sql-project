CREATE SCHEMA spotify_sql;

USE spotify_sql;

CREATE TABLE tracks (
	track_id INT NOT NULL AUTO_INCREMENT,
    track_name VARCHAR(100) NOT NULL,
    released_year YEAR,
    duration FLOAT,
    album VARCHAR(100),
    streams VARCHAR(50),
    PRIMARY KEY (track_id)
);


CREATE TABLE artists (
	artist_id INT NOT NULL AUTO_INCREMENT,
    artist_name VARCHAR(100) NOT NULL,
    genre VARCHAR(200),
    PRIMARY KEY (artist_id)
);

CREATE TABLE song_attributes (
	id INT NOT NULL AUTO_INCREMENT,
    bpm INT,
    track_key VARCHAR(10),
    track_mode VARCHAR(20),
    danceability INT,
    valence INT,
    energy INT,
    acousticness INT,
    instrumentalness INT,
    liveness INT,
    speechiness INT,
    PRIMARY KEY (id)
);

    
